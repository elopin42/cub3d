#include "../libft.h"

int	ft_strstr(char *b, char *li)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (!*li)
		return (0);
	while (b[i])
	{
		j = 0;
		while (li[j] == b[i] && b[i])
		{
			i++;
			j++;
		}
		if (!li[j])
			return (1);
		i -= j;
		i++;
	}
	return (0);
}
