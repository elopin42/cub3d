#include "../libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*str;
	size_t	i;

	i = 0;
	if (!s)
		return (NULL);
	if (start >= ft_strlen(s))
		return (ft_strdup(""));
	if (start + len > ft_strlen(s))
		len = ft_strlen(s) - start;
	str = malloc(((len + 1) * sizeof (char)));
	if (!str)
		return (NULL);
	while (i < len)
		str[i++] = ((char *)s)[start++];
	str[i] = '\0';
	return (str);
}
