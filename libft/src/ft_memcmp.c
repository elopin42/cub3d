#include "../libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char	*tmp1;
	unsigned char	*tmp2;
	size_t			i;

	tmp1 = (void *)s1;
	tmp2 = (void *)s2;
	i = 0;
	if (n == 0)
		return (0);
	while (i < n && *tmp1 == *tmp2)
	{
		tmp1++;
		tmp2++;
		i++;
	}
	if (i == n)
		return (0);
	return (*tmp1 - *tmp2);
}
